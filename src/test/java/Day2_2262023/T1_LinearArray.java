package Day2_2262023;

public class T1_LinearArray {
    public static void main(String[] args) {
        //create a list of cities in linear array
        //array count starts from 0 index
        String[] cities = new String[] {"Brooklyn","Queens","Manhattan","Bronx","Staten Island"};
        System.out.println("Cities count "+ cities.length );//this will get the count of the list of cities
        System.out.println("My cities are " + cities[1]+ ", " + cities[2]); //This will print out Queens and Manhattan

        //Create a list of zipcodes
        int[] zipcode = new int[] {11431, 11432, 11433, 11434};
        System.out.println("Zipcode count "+zipcode.length);
        System.out.println("My zipcode is " + zipcode[1]);





    }


}
